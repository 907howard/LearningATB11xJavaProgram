package Tasks_April.April8thTask;

public class For_loop_Excercise4
{

    public static void main(String[] args) {
//4️⃣ Print the sum of first 10 natural numbers
        int sum=0;
        for(int i=1;i<=10; i++){
            sum = sum +i;
            System.out.println(i);
            System.out.println("Sum of natural numbers from 10 to is " + sum);
        }
    }
}
