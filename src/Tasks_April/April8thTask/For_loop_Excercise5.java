package Tasks_April.April8thTask;

public class For_loop_Excercise5 {
    public static void main(String[] args) {
//5️⃣ Print the multiplication table of 5
        int mul =1;
        for(int i=1;i<=5; i++){
             mul = mul * i;
            System.out.println(i);
            System.out.println("Multiplication of numbers from 1 to 5 is " + mul);
        }
    }
}
